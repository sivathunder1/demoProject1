package java.feq.questions;

public class PrintingDoubleQuotes_19 {
	public static void main(String[] args) {
		String stringWithDoubleQuotes = "This is a string with \"double quotes\".";
		System.out.println(stringWithDoubleQuotes);
		String stringWithSingleQuotes = "This is a string with a single quote: 'Inside	single quotes'";
		System.out.println(stringWithSingleQuotes);
		String stringWithForwardSlash = "This is a string with a forward slash:/path/to/something";
		System.out.println(stringWithForwardSlash);
		String stringWithBackslash = "This is a string with a backslash:C:\\Windows\\System32";
		System.out.println(stringWithBackslash);
	}
}
