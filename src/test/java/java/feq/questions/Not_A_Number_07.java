public class Not_A_Number_07 {
	// What is NAN in a programming language
	public static void main(String[] args) {
		System.out.println(0.0/0.0);
		System.out.println(Math.sqrt(-1));
		System.out.println(Float.NaN == Float.NaN);
		System.out.println(Float.NaN != Float.NaN);
	}
}

